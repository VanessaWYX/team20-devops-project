<?php 
namespace calc;
class calculator
{
    function add($numOne,$num2){
        return ($numOne + $num2);
    }
    function subtract($num1,$num2){
        return ($num1 - $num2);
    }
    function multiply($num1,$num2){
        return ($num1 * $num2);
    }
    function divide($num1,$num2){
        return ($num1 / $num2);
    }
}
